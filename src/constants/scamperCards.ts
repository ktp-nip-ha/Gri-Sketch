/**
 * 全てのカードをフラットな配列として取得する関数
 * 将来的に「アンロック済み」などのフィルタリングをここに追加しやすい構造にしています。
 */
export const getAllScamperCards = (): ScamperCard[] => {
  return Object.entries(SCAMPER_CARDS_DATA).flatMap(([category, data]) => 
    data.items.map((item, index) => ({
      id: `${category}-${index}`,
      category,
      categoryLabel: data.label,
      text: item
    }))
  );
};
